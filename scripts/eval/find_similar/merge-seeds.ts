#!/usr/bin/env tsx
// Seed-set dedupe + merge (M5 entry calibration helper).
//
// Combines the original 21-seed nino-authored set with three lens-flavored
// 30-seed sets authored by separate agent instances (per BRD-OPEN-QUESTIONS
// section 26 mitigation). Dedupes by query-token Jaccard similarity:
//   - Tokenize each query to lowercased alphanumeric tokens of length >= 3.
//   - Pairwise Jaccard similarity >= 0.7 -> consider duplicate; keep the
//     earlier (more nino-vintage) entry, drop the later.
//   - Also drop entries whose query, after normalization, is identical
//     character-by-character.
//
// Output goes to atelier/eval/find_similar/seeds-merged.yaml. The runner
// can be pointed at it via --seed-file; switching the canonical seeds.yaml
// to the merged set is a separate decision documented in the M5 PR.

import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { join, resolve } from 'node:path';

import { parse as parseYaml, stringify as yamlStringify } from 'yaml';

interface Seed {
  id: string;
  description?: string;
  query: string;
  trace_id?: string;
  expected: string[];
}

const REPO_ROOT = resolve(process.cwd());
const EVAL_DIR = join(REPO_ROOT, 'atelier/eval/find_similar');

const SOURCES = [
  { path: join(EVAL_DIR, 'seeds.yaml'), label: 'original (cleaned)' },
  { path: join(EVAL_DIR, 'seeds-analyst.yaml'), label: 'analyst-lens' },
  { path: join(EVAL_DIR, 'seeds-dev.yaml'), label: 'dev-lens' },
  { path: join(EVAL_DIR, 'seeds-pm.yaml'), label: 'pm-lens' },
];

const JACCARD_DUP_THRESHOLD = 0.7;
const MIN_TOKEN_LEN = 3;

function tokenize(query: string): Set<string> {
  return new Set(
    query
      .toLowerCase()
      .split(/[^a-z0-9_]+/)
      .map((t) => t.replace(/[^a-z0-9]/g, ''))
      .filter((t) => t.length >= MIN_TOKEN_LEN),
  );
}

function jaccard(a: Set<string>, b: Set<string>): number {
  if (a.size === 0 && b.size === 0) return 1;
  let intersection = 0;
  for (const t of a) if (b.has(t)) intersection += 1;
  const union = a.size + b.size - intersection;
  return union === 0 ? 0 : intersection / union;
}

interface RecordedSeed extends Seed {
  source: string;
  tokens: Set<string>;
}

function main(): void {
  const all: RecordedSeed[] = [];
  for (const src of SOURCES) {
    if (!existsSync(src.path)) {
      console.warn(`[merge-seeds] ${src.path} not found; skipping`);
      continue;
    }
    const parsed = parseYaml(readFileSync(src.path, 'utf8')) as { seeds?: Seed[] };
    if (!parsed?.seeds) continue;
    for (const seed of parsed.seeds) {
      all.push({ ...seed, source: src.label, tokens: tokenize(seed.query) });
    }
  }

  const kept: RecordedSeed[] = [];
  const dropped: Array<{ id: string; reason: string }> = [];
  const idsSeen = new Set<string>();

  for (const candidate of all) {
    if (idsSeen.has(candidate.id)) {
      dropped.push({ id: candidate.id, reason: `duplicate id with kept seed` });
      continue;
    }
    let conflict: { id: string; sim: number } | null = null;
    for (const existing of kept) {
      const sim = jaccard(candidate.tokens, existing.tokens);
      if (sim >= JACCARD_DUP_THRESHOLD) {
        conflict = { id: existing.id, sim };
        break;
      }
    }
    if (conflict) {
      dropped.push({
        id: candidate.id,
        reason: `Jaccard ${conflict.sim.toFixed(2)} vs kept ${conflict.id}`,
      });
      continue;
    }
    kept.push(candidate);
    idsSeen.add(candidate.id);
  }

  // Strip the runtime-only fields before writing.
  const out = {
    seeds: kept.map(({ source: _s, tokens: _t, ...rest }) => rest),
  };
  const outPath = join(EVAL_DIR, 'seeds-merged.yaml');
  writeFileSync(
    outPath,
    `# Merged seed set (M5 entry; BRD-OPEN-QUESTIONS section 26 mitigation).
# Original 21 (nino-authored, cleaned per M5-AUDIT.md) + 30 analyst-lens +
# 30 dev-lens + 30 pm-lens (each authored by separate agent instances with
# distinct ADR-017 lens priming) -> deduped by query-token Jaccard >= 0.7.
# Generated by scripts/eval/find_similar/merge-seeds.ts; do not hand-edit.
${yamlStringify(out, { lineWidth: 120 })}`,
  );

  console.log(`[merge-seeds] kept ${kept.length} of ${all.length} (${dropped.length} dropped)`);
  if (dropped.length > 0) {
    console.log('[merge-seeds] drops:');
    for (const d of dropped) console.log(`  ${d.id}  ${d.reason}`);
  }
  // Per-source breakdown
  const bySource: Record<string, number> = {};
  for (const s of kept) bySource[s.source] = (bySource[s.source] ?? 0) + 1;
  console.log('[merge-seeds] kept-per-source:');
  for (const [src, count] of Object.entries(bySource)) console.log(`  ${src}: ${count}`);
  console.log(`[merge-seeds] wrote ${outPath}`);
}

main();
